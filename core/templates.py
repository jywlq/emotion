DREAM_CLOSINGS = [
    "🌌 坐标: RA {ra}h {ram}m | Dec {dec}° {decm}'",
    "🌌 信号频率: {freq} GHz | 星域: {sector}",
    "🌌 接收时间: 星历 {stardate} | 频道: {channel}",
]


def get_personality_reading(trait: str, score: int) -> str:
    """根据性格维度和分数返回解读文本"""
    templates = PERSONALITY_TEMPLATES.get(trait, PERSONALITY_TEMPLATES["温柔"])

    if score >= 4:
        pool = templates.get("high", templates.get("mid", []))
    elif score >= 2:
        pool = templates.get("mid", templates.get("high", []))
    else:
        pool = templates.get("low", templates.get("mid", []))

    return random.choice(pool) if pool else "你是宇宙中独一无二的存在。"


def get_archetype_description(archetype: str) -> str:
    """获取原型综合描述"""
    pool = ARCHETYPE_DESCRIPTIONS.get(archetype, ARCHETYPE_DESCRIPTIONS["织梦者"])
    return random.choice(pool)


def get_mood_advice(mood_key: str) -> str:
    """获取情绪建议"""
    pool = MOOD_ADVICE.get(mood_key, MOOD_ADVICE["default"])
    return random.choice(pool)


def get_dream_coordinates() -> dict:
    """生成随机梦境星际坐标"""
    return {
        "ra": random.randint(0, 23),
        "ram": random.randint(0, 59),
        "dec": random.randint(-90, 90),
        "decm": random.randint(0, 59),
        "freq": round(random.uniform(1.0, 1420.0), 3),
        "sector": random.choice(["α-7", "β-12", "γ-3", "δ-9", "ε-1", "ζ-5", "η-2"]),
        "stardate": round(random.uniform(74000, 79000), 1),
        "channel": random.choice(["θ波", "δ波", "α波", "γ波"]),
    }


def format_dream_coordinates(coords: dict) -> str:
    """格式化梦境坐标输出"""
    template = random.choice(DREAM_CLOSINGS)
    try:
        return template.format(**coords)
    except KeyError:
        return f"🌌 坐标: RA {coords.get('ra', '??')}h | Dec {coords.get('dec', '??')}°"
